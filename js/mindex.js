// 渲染新闻列表函数
function renderNewsList(newsItems) {
    const $container = $('.company-news-line-list ul');
    
    // 遍历新闻数据
    $.each(newsItems, function(index, news) {
        // 创建新闻卡片元素
        const $newsCard = $(`

        	<li>
	            <div class="company-news-line-list-title">
	              <a href="${news.url}" target="_blank">${news.title}</a>
	            </div>  
	            <div class="company-news-line-list-time">${news.time}</div>
	         </li> 
        `);

        // 添加到容器
        $container.append($newsCard);

        changePC('.company-news-line-list', width, 1600, 600, 50, 180);
        changePC('.company-news-line-list li', width, 1600, 80, 0, 60);
        changePC('.company-news-line-list-title', width, 1600, 60, 0, 0);
        changePCSize('.company-news-line-list-title', width, 40);
        changePCLineSize('.company-news-line-list-title', width, 60);
        changePCSize('.company-news-line-list-time', width, 35);
        changePCLineSize('.company-news-line-list-time', width, 50);
    });
}


